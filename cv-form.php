		function surligne(champ, erreur)
		{
		   if(erreur)
		      champ.style.backgroundColor = "#FFA8A8";
		   else
		      champ.style.backgroundColor = "";
		}
		function ft_verif_mail(champ)
		{
		   var regex = /^[a-zA-Z0-9._-]+@[a-z0-9._-]{2,}\.[a-z]{2,4}$/;
		   if(!regex.test(champ.value))
		   {
		      surligne(champ, true);
		      return false;
		   }
		   else
		   {
		      surligne(champ, false);
		      return true;
		   }
		}
		function ft_verif_date(champ)
		{
		   var regex = /^(((0[1-9]|[12]\d|3[01])\/(0[13578]|1[02])\/((19|[2-9]\d)\d{2}))|((0[1-9]|[12]\d|30)\/(0[13456789]|1[012])\/((19|[2-9]\d)\d{2}))|((0[1-9]|1\d|2[0-8])\/02\/((19|[2-9]\d)\d{2}))|(29\/02\/((1[6-9]|[2-9]\d)(0[48]|[2468][048]|[13579][26])|((16|[2468][048]|[3579][26])00))))$/;
		   if(!regex.test(champ.value))
		   {
		      surligne(champ, true);
		      return false;
		   }
		   else
		   {
		      surligne(champ, false);
		      return true;
		   }
		}
